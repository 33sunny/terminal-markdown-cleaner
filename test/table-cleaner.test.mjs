    '  │ 一  │ 惨是强的压力测 │ 越惨还能撑住 = 他是真强   │',
    '  │ 层  │ 试             │                           │',
    '  ├─────┼────────────────┼───────────────────────────┤',
    '  │ 二  │ 惨是脆弱的权限 │ 他只在她面前露出这一面 =  │',
    '  │ 层  │                │ 她是唯一的                │',
